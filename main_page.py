st.markdown("## Introduction")
st.write("Modelling the interactions between protein and small molecule ligands is crucial for structure-based drug design. While significant advances have been made in predicting apo-protein structures from sequence with deep learning, obtaining co-folded protein-ligand complex structures would be one step forward to AI-based drug discovery. Current available models achieving the goal of protein-ligand co-folding from sequence information include Umol and RoseTTAFold All-Atom. In their respective publications, the performance of these models were tested on public available datasets, which included endogenous ligands and don’t fully reflect drug-discovery applications. This caveat motivates us to benchmark these methods on ligand-protein co-crystallized structures from the Novartis internal Proasis database to assess their performance and determine suitability for internal drug discovery programs. ")
